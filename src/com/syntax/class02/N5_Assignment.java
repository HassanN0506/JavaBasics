package com.syntax.class02;

public class N5_Assignment {

	public static void main(String[] args) {
		
		double d1=1.1;
		double d2=2.2;
		double add=d1+d2;
		double sub=d1-d2;
		double mul=d1*d2;
		double div=d1/d2;
		System.out.println("The addition of "+d1+" and "+d2+" is "+add);
		System.out.println("The subtraction of "+d1+" and "+d2+" is "+sub);
		System.out.println("The multiplication of "+d1+" and "+d2+" is "+mul);
		System.out.println("The division of "+d1+" and "+d2+" is "+div);
		
		double num1=3.9;
		double sq1=num1*num1;
		System.out.println("The square of "+num1+" is "+sq1);
		
		int w=5;
		int h=8;
		int area=w*h;
		int perimeter=w+w+h+h;
		System.out.println("The perimeter of a rectangle with the width of "+w+" and height "+h+" is "+perimeter+" and the area is "+area);
		
	}

}
