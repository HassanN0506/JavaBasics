public class N1_Notes2_6 {

	public static void main(String[] args) {
		
		/*
		 * while loop:
			 while (condition){
			 	code;
			 }
			 
			&
			do while loop:
			
			do{
				code;
			} while (condition);
			
			---------------------------------
			for loop:
			 
			 for(initialization; condition; increment/decrement){
			 	code;
			 }
			 
			 we use for loop when the number of repetition is known
			 
			 we use while/do while when we DO NOT KNOW the number of repetitions needed 
			  
			  
			 ---------------------------------------------------
			 NESTED LOOP --> loop inside another loop
			 
			 we dont try to nest more than 2 or 3 loops to avoid messiness
		 */
		

		/* 
		 * Break → breaks the loop
		 * Continue → skips the current iteration
		 * 
		 * break and continue cannot coexist within the same exact block of code
		 * because whatever comes first will be executed,
		 * and whatever comes second will always be ignored
		 */
		
	}

}
